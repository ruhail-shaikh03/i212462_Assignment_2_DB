create table Person
	(SSN NUMERIC(3) NOT NULL,
	 PNAME VARCHAR(15),
	 PHONENO NUMERIC(11), 
	 PADDRESS VARCHAR(15), 
	 RTYPE VARCHAR(15)
	)
	
create table Emp
	(SSN NUMERIC(3) NOT NULL,
	SALARY NUMERIC(8), 
	ESHIFT VARCHAR(10),
	RESTC VARCHAR(15),
	SSTART TIME,
	S_END TIME
	)

create table Pilot
	(SSN NUMERIC(3) NOT NULL,
	LIC_NUM NUMERIC(20), 
	RESTC VARCHAR(15)
	)

create table Own
	(SSN NUMERIC(3) NOT NULL,
	PLANE_REG NUMERIC(4),
	PDATE DATE
	)

create table Corporation
	(SSN NUMERIC(3) NOT NULL,
	 CNAME VARCHAR(15),
	 CHONENO NUMERIC(11), 
	 CADDRESS VARCHAR(15),
	)


create table Plane
	(REGNO NUMERIC(4) NOT NULL,
	 OF_TYPE VARCHAR(15),
	 STORED_IN NUMERIC(4)	
	 )

create table P_Type
	(REGNO NUMERIC(5) NOT NULL,
	 MODEL VARCHAR(10),
	 PCAP NUMERIC(6),
	 PWEIGHT NUMERIC(6)
	)


create table Hanger
	(HNumber NUMERIC(5) NOT NULL,
	 HCAP NUMERIC(3),
	 HLOC VARCHAR(15)
	)

create table SERV
	(REGNO NUMERIC(5) NOT NULL,
	SSN NUMERIC(3) NOT NULL,
	SHOURS NUMERIC(2),
	MDATE DATE,
	WTYPE VARCHAR(30)
	)

